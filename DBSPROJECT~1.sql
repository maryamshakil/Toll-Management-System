-- =========================================================
-- 15. EXEMPTION (ER Diagram)
-- =========================================================
CREATE TABLE EXEMPTION (
    EXEMPTION_ID        NUMBER PRIMARY KEY,
    USER_ID             NUMBER NOT NULL,
    PLAZA_ID            NUMBER,
    REASON              VARCHAR2(200),
    START_DATE          DATE,
    END_DATE            DATE,
    DISCOUNT_TYPE       VARCHAR2(20),
    DISCOUNT_VALUE      NUMBER(10,2),
    STATUS              VARCHAR2(20) DEFAULT 'ACTIVE',
    CONSTRAINT FK_EX_USER FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID),
    CONSTRAINT FK_EX_PLAZA FOREIGN KEY (PLAZA_ID) REFERENCES TOLL_PLAZA(PLAZA_ID)
