-- =========================================================
-- ONE TAG ONE NATION - CREATE ALL TABLES (17 CORE + ER EXTENSIONS)
-- Oracle SQL
-- =========================================================

-- =========================================================
-- 1. USERS
-- =========================================================
CREATE TABLE USERS (
    USER_ID             NUMBER PRIMARY KEY,
    NAME                VARCHAR2(100) NOT NULL,
    CNIC                VARCHAR2(15) UNIQUE NOT NULL,
    PHONE               VARCHAR2(15),
    EMAIL               VARCHAR2(100),
    PROVINCE            VARCHAR2(50),
    ADDRESS             VARCHAR2(200),
    EMERGENCY_CONTACT   VARCHAR2(15),
    IS_ACTIVE           CHAR(1) DEFAULT 'Y' CHECK (IS_ACTIVE IN ('Y','N')),
    USERNAME            VARCHAR2(50) UNIQUE,
    PASSWORD            VARCHAR2(50)
);

-- =========================================================
-- 2. ROLE & USER_ROLE (ER Diagram)
-- =========================================================
CREATE TABLE ROLE (
    ROLE_ID             NUMBER PRIMARY KEY,
    ROLE_NAME           VARCHAR2(50) NOT NULL,
    DESCRIPTION         VARCHAR2(200)
);

CREATE TABLE USER_ROLE (
    USER_ROLE_ID        NUMBER PRIMARY KEY,
    USER_ID             NUMBER NOT NULL,
    ROLE_ID             NUMBER NOT NULL,
    ASSIGNED_DATE       DATE DEFAULT SYSDATE,
    CONSTRAINT FK_UR_USER FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID),
    CONSTRAINT FK_UR_ROLE FOREIGN KEY (ROLE_ID) REFERENCES ROLE(ROLE_ID)
);

-- =========================================================
-- 3. DRIVER
-- =========================================================
CREATE TABLE DRIVER (
    DRIVER_CNIC         VARCHAR2(15) PRIMARY KEY,
    DRIVER_NAME         VARCHAR2(100) NOT NULL,
    LICENSE_NO          VARCHAR2(30) UNIQUE,
    DRIVER_PHONE        VARCHAR2(15),
    LICENSE_EXPIRY      DATE
);

-- =========================================================
-- 4. VEHICLE_TYPE
-- =========================================================
CREATE TABLE VEHICLE_TYPE (
    TYPE_ID             NUMBER PRIMARY KEY,
    VEHICLE_CLASS       VARCHAR2(50) NOT NULL,
    TOLL_RATE_PER_KM    NUMBER(10,2) NOT NULL
);

-- =========================================================
-- 5. VEHICLE (Supertype)
-- =========================================================
CREATE TABLE VEHICLE (
    VEHICLE_ID          NUMBER PRIMARY KEY,
    USER_ID             NUMBER NOT NULL,
    DRIVER_CNIC         VARCHAR2(15),
    TYPE_ID             NUMBER NOT NULL,
    PLATE_NUMBER        VARCHAR2(20) UNIQUE NOT NULL,
    MODEL               VARCHAR2(50),
    COMPANY             VARCHAR2(50),
    COLOR               VARCHAR2(30),
    IS_BLACKLISTED      CHAR(1) DEFAULT 'N' CHECK (IS_BLACKLISTED IN ('Y','N')),
    CONSTRAINT FK_V_USER FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID),
    CONSTRAINT FK_V_DRIVER FOREIGN KEY (DRIVER_CNIC) REFERENCES DRIVER(DRIVER_CNIC),
    CONSTRAINT FK_V_TYPE FOREIGN KEY (TYPE_ID) REFERENCES VEHICLE_TYPE(TYPE_ID)
);

-- Vehicle ISA Subtypes (ER Diagram)
CREATE TABLE VEHICLE_CAR_JEEP_BUS (
    VEHICLE_ID          NUMBER PRIMARY KEY,
    SEATS               NUMBER,
    AXLES               NUMBER DEFAULT 2,
    CONSTRAINT FK_VCJB_VEHICLE FOREIGN KEY (VEHICLE_ID) REFERENCES VEHICLE(VEHICLE_ID)
);

CREATE TABLE VEHICLE_TRUCK (
    VEHICLE_ID          NUMBER PRIMARY KEY,
    NO_OF_AXLES         NUMBER NOT NULL,
    LOAD_CAPACITY       NUMBER(10,2),
    CONSTRAINT FK_VT_VEHICLE FOREIGN KEY (VEHICLE_ID) REFERENCES VEHICLE(VEHICLE_ID)
);

CREATE TABLE VEHICLE_MOTORCYCLE (
    VEHICLE_ID          NUMBER PRIMARY KEY,
    ENGINE_CAPACITY     NUMBER,
    CONSTRAINT FK_VM_VEHICLE FOREIGN KEY (VEHICLE_ID) REFERENCES VEHICLE(VEHICLE_ID)
);

-- =========================================================
-- 6. TAG
-- =========================================================
CREATE TABLE TAG (
    TAG_ID              NUMBER PRIMARY KEY,
    USER_ID             NUMBER NOT NULL,
    VEHICLE_ID          NUMBER NOT NULL,
    BALANCE             NUMBER(10,2) DEFAULT 0,
    RESERVE_BALANCE     NUMBER(10,2) DEFAULT 0,
    STATUS              VARCHAR2(20) DEFAULT 'ACTIVE',
    ISSUE_DATE          DATE DEFAULT SYSDATE,
    EXPIRY_DATE         DATE,
    CONSTRAINT FK_TAG_USER FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID),
    CONSTRAINT FK_TAG_VEHICLE FOREIGN KEY (VEHICLE_ID) REFERENCES VEHICLE(VEHICLE_ID)
);

-- =========================================================
-- 7. MOTORWAY & HIGHWAY (ER Diagram hierarchy)
-- =========================================================
CREATE TABLE MOTORWAY (
    MOTORWAY_ID         NUMBER PRIMARY KEY,
    MOTORWAY_NAME       VARCHAR2(100) NOT NULL,
    START_CITY          VARCHAR2(100),
    END_CITY            VARCHAR2(100),
    TOTAL_DISTANCE_KM   NUMBER(10,2)
);

CREATE TABLE HIGHWAY (
    HIGHWAY_ID          NUMBER PRIMARY KEY,
    MOTORWAY_ID         NUMBER NOT NULL,
    HIGHWAY_NAME        VARCHAR2(100) NOT NULL,
    SECTION_NAME        VARCHAR2(100),
    DIRECTION           VARCHAR2(20),
    START_LOCATION      VARCHAR2(100),
    END_LOCATION        VARCHAR2(100),
    TOTAL_DISTANCE      NUMBER(10,2),
    CONSTRAINT FK_HW_MOTORWAY FOREIGN KEY (MOTORWAY_ID) REFERENCES MOTORWAY(MOTORWAY_ID)
);

-- =========================================================
-- 8. HIGHWAY_UPDATE
-- =========================================================
CREATE TABLE HIGHWAY_UPDATE (
    UPDATE_ID           NUMBER PRIMARY KEY,
    HIGHWAY_ID          NUMBER NOT NULL,
    SURCHARGE           NUMBER(10,2) DEFAULT 0,
    UPDATE_DATE         DATE DEFAULT SYSDATE,
    CONSTRAINT FK_HU_HIGHWAY FOREIGN KEY (HIGHWAY_ID) REFERENCES HIGHWAY(HIGHWAY_ID)
);

-- =========================================================
-- 9. SERVICE
-- =========================================================
CREATE TABLE SERVICE (
    SERVICE_ID          NUMBER PRIMARY KEY,
    SERVICE_NAME        VARCHAR2(100) NOT NULL,
    SERVICE_TYPE        VARCHAR2(50),
    TOLL_RATE_PER_KM    NUMBER(10,2),
    BASE_RATE_PER_KM    NUMBER(10,2),
    SURCHARGE_RATE      NUMBER(10,2) DEFAULT 0,
    EFFECTIVE_FROM      DATE DEFAULT SYSDATE
);

-- =========================================================
-- 10. TOLL_PLAZA
-- =========================================================
CREATE TABLE TOLL_PLAZA (
    PLAZA_ID            NUMBER PRIMARY KEY,
    HIGHWAY_ID          NUMBER NOT NULL,
    SERVICE_ID          NUMBER,
    PLAZA_NAME          VARCHAR2(100) NOT NULL,
    LOCATION            VARCHAR2(100),
    PROVINCE            VARCHAR2(50),
    LATITUDE            NUMBER(10,6),
    LONGITUDE           NUMBER(10,6),
    EXIT_NO             VARCHAR2(20),
    MILE_NO             NUMBER(10,2),
    CONSTRAINT FK_TP_HIGHWAY FOREIGN KEY (HIGHWAY_ID) REFERENCES HIGHWAY(HIGHWAY_ID),
    CONSTRAINT FK_TP_SERVICE FOREIGN KEY (SERVICE_ID) REFERENCES SERVICE(SERVICE_ID)
);

-- =========================================================
-- 11. TERMINAL (IN/OUT) - ER Diagram
-- =========================================================
CREATE TABLE TERMINAL (
    TERMINAL_ID         NUMBER PRIMARY KEY,
    PLAZA_ID            NUMBER NOT NULL,
    TERMINAL_NAME       VARCHAR2(100),
    TERMINAL_TYPE       VARCHAR2(10) CHECK (TERMINAL_TYPE IN ('IN','OUT')),
    MILE_NO             NUMBER(10,2),
    DISTANCE_FROM_PREV  NUMBER(10,2),
    STATUS              VARCHAR2(20) DEFAULT 'ACTIVE',
    CONSTRAINT FK_TERM_PLAZA FOREIGN KEY (PLAZA_ID) REFERENCES TOLL_PLAZA(PLAZA_ID)
);

CREATE TABLE IN_TERMINAL (
    TERMINAL_ID         NUMBER PRIMARY KEY,
    LANE_NO             NUMBER,
    READER_IP           VARCHAR2(50),
    CAMERA_IP           VARCHAR2(50),
    CONSTRAINT FK_IN_TERM FOREIGN KEY (TERMINAL_ID) REFERENCES TERMINAL(TERMINAL_ID)
);

CREATE TABLE OUT_TERMINAL (
    TERMINAL_ID         NUMBER PRIMARY KEY,
    LANE_NO             NUMBER,
    READER_IP           VARCHAR2(50),
    CAMERA_IP           VARCHAR2(50),
    CONSTRAINT FK_OUT_TERM FOREIGN KEY (TERMINAL_ID) REFERENCES TERMINAL(TERMINAL_ID)
);

-- =========================================================
-- 12. EMPLOYEE
-- =========================================================
CREATE TABLE EMPLOYEE (
    EMPLOYEE_ID         NUMBER PRIMARY KEY,
    EMPLOYEE_NAME       VARCHAR2(100) NOT NULL,
    PHONE               VARCHAR2(15),
    DESIGNATION         VARCHAR2(50),
    PLAZA_ID            NUMBER,
    CONSTRAINT FK_EMP_PLAZA FOREIGN KEY (PLAZA_ID) REFERENCES TOLL_PLAZA(PLAZA_ID)
);

-- =========================================================
-- 13. CAMERA_SYSTEM
-- =========================================================
CREATE TABLE CAMERA_SYSTEM (
    CAMERA_ID           NUMBER PRIMARY KEY,
