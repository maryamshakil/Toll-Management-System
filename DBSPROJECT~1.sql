-- =========================================================
-- ONE TAG ONE NATION - VIEWS
-- =========================================================

CREATE OR REPLACE VIEW VEHICLE_TAG_DETAILS AS
SELECT
    U.NAME              AS OWNER_NAME,
    U.CNIC              AS OWNER_CNIC,
    V.PLATE_NUMBER,
    VT.VEHICLE_CLASS,
    VT.TOLL_RATE_PER_KM,
    T.TAG_ID,
    T.BALANCE,
    T.RESERVE_BALANCE,
    T.STATUS            AS TAG_STATUS
FROM VEHICLE V
JOIN TAG T ON V.VEHICLE_ID = T.VEHICLE_ID
JOIN USERS U ON V.USER_ID = U.USER_ID
JOIN VEHICLE_TYPE VT ON V.TYPE_ID = VT.TYPE_ID;

CREATE OR REPLACE VIEW TRANSACTION_SUMMARY AS
SELECT
    TH.TRANSACTION_ID,
    T.TAG_ID,
