SELECT U.NAME, AVG(F.RATING) AS AVG_RATING
FROM USERS U
JOIN FEEDBACK F ON U.USER_ID = F.USER_ID
GROUP BY U.NAME
HAVING AVG(F.RATING) >= 4;

