-- =========================================================
-- 16. TRANSACTION_HISTORY (Enhanced per ER Diagram)
-- =========================================================
CREATE TABLE TRANSACTION_HISTORY (
    TRANSACTION_ID      NUMBER PRIMARY KEY,
    TAG_ID              NUMBER NOT NULL,
    SERVICE_ID          NUMBER,
    PLAZA_ID            NUMBER,
    ENTRY_TERMINAL_ID   NUMBER,
    EXIT_TERMINAL_ID    NUMBER,
    ENTRY_TIME          TIMESTAMP,
