-- =========================================================
-- ONE TAG ONE NATION - PROCEDURES & FUNCTIONS
-- Toll Calculation per ER Diagram:
--   Distance = Exit MileNo - Entry MileNo
--   Base Toll = Distance x TollRatePerKm
--   Total Toll = Base Toll + Surcharge - Exemption Discount
--   Final Payable = MAX(Total Toll, 0)
-- =========================================================

-- Function: Get tag balance
CREATE OR REPLACE FUNCTION GET_BALANCE(P_TAG NUMBER)
RETURN NUMBER
IS
    V_BALANCE NUMBER;
BEGIN
    SELECT BALANCE INTO V_BALANCE
    FROM TAG WHERE TAG_ID = P_TAG;
    RETURN V_BALANCE;
EXCEPTION
    WHEN NO_DATA_FOUND THEN RETURN NULL;
END;
/

-- Function: Calculate toll amount
CREATE OR REPLACE FUNCTION CALCULATE_TOLL(
    P_ENTRY_MILE    NUMBER,
    P_EXIT_MILE     NUMBER,
    P_RATE_PER_KM   NUMBER,
    P_SURCHARGE     NUMBER DEFAULT 0,
    P_DISCOUNT      NUMBER DEFAULT 0
)
RETURN NUMBER
IS
    V_DISTANCE      NUMBER;
    V_BASE_TOLL     NUMBER;
    V_TOTAL         NUMBER;
BEGIN
    V_DISTANCE  := GREATEST(P_EXIT_MILE - P_ENTRY_MILE, 0);
    V_BASE_TOLL := V_DISTANCE * P_RATE_PER_KM;
    V_TOTAL     := V_BASE_TOLL + NVL(P_SURCHARGE,0) - NVL(P_DISCOUNT,0);
    RETURN GREATEST(V_TOTAL, 0);
END;
/

-- Procedure: Simple toll deduction
CREATE OR REPLACE PROCEDURE DEDUCT_TOLL(
    P_TAG_ID    NUMBER,
    P_AMOUNT    NUMBER
)
IS
BEGIN
    UPDATE TAG
    SET BALANCE = BALANCE - P_AMOUNT
    WHERE TAG_ID = P_TAG_ID;

    IF SQL%ROWCOUNT = 0 THEN
        RAISE_APPLICATION_ERROR(-20001, 'Tag not found: ' || P_TAG_ID);
    END IF;

    DBMS_OUTPUT.PUT_LINE('Toll deducted: ' || P_AMOUNT || ' from Tag ' || P_TAG_ID);
END;
/

-- Procedure: Full toll transaction flow (ER Diagram)
CREATE OR REPLACE PROCEDURE PROCESS_TOLL_TRANSACTION(
    P_TAG_ID            NUMBER,
    P_SERVICE_ID        NUMBER,
    P_PLAZA_ID          NUMBER,
    P_ENTRY_TERMINAL    NUMBER,
    P_EXIT_TERMINAL     NUMBER,
    P_ENTRY_TIME        TIMESTAMP,
    P_EXIT_TIME         TIMESTAMP
)
IS
    V_ENTRY_MILE        NUMBER;
    V_EXIT_MILE         NUMBER;
    V_RATE              NUMBER;
    V_SURCHARGE         NUMBER;
    V_DISCOUNT          NUMBER;
    V_DISTANCE          NUMBER;
    V_BASE_TOLL         NUMBER;
    V_TOTAL             NUMBER;
    V_BALANCE           NUMBER;
    V_USER_ID           NUMBER;
    V_TXN_ID            NUMBER;
BEGIN
    -- Get mile markers from terminals
    SELECT MILE_NO INTO V_ENTRY_MILE FROM TERMINAL WHERE TERMINAL_ID = P_ENTRY_TERMINAL;
    SELECT MILE_NO INTO V_EXIT_MILE FROM TERMINAL WHERE TERMINAL_ID = P_EXIT_TERMINAL;

    -- Get vehicle rate from tag -> vehicle -> vehicle_type
    SELECT VT.TOLL_RATE_PER_KM, T.USER_ID, T.BALANCE
    INTO V_RATE, V_USER_ID, V_BALANCE
    FROM TAG T
    JOIN VEHICLE V ON T.VEHICLE_ID = V.VEHICLE_ID
    JOIN VEHICLE_TYPE VT ON V.TYPE_ID = VT.TYPE_ID
    WHERE T.TAG_ID = P_TAG_ID;

    -- Get surcharge from highway update (latest)
    BEGIN
        SELECT NVL(HU.SURCHARGE, 0) INTO V_SURCHARGE
        FROM TOLL_PLAZA TP
        JOIN HIGHWAY_UPDATE HU ON TP.HIGHWAY_ID = HU.HIGHWAY_ID
        WHERE TP.PLAZA_ID = P_PLAZA_ID
        ORDER BY HU.UPDATE_DATE DESC
        FETCH FIRST 1 ROW ONLY;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN V_SURCHARGE := 0;
    END;

    -- Get exemption discount if applicable
    BEGIN
        SELECT
            CASE E.DISCOUNT_TYPE
                WHEN 'PERCENT' THEN (V_EXIT_MILE - V_ENTRY_MILE) * V_RATE * E.DISCOUNT_VALUE / 100
                WHEN 'FIXED'   THEN E.DISCOUNT_VALUE
                ELSE 0
            END
        INTO V_DISCOUNT
        FROM EXEMPTION E
        WHERE E.USER_ID = V_USER_ID
          AND (E.PLAZA_ID = P_PLAZA_ID OR E.PLAZA_ID IS NULL)
          AND E.STATUS = 'ACTIVE'
          AND SYSDATE BETWEEN E.START_DATE AND E.END_DATE
        FETCH FIRST 1 ROW ONLY;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN V_DISCOUNT := 0;
    END;

    -- Calculate toll
    V_DISTANCE  := GREATEST(V_EXIT_MILE - V_ENTRY_MILE, 0);
    V_BASE_TOLL := V_DISTANCE * V_RATE;
    V_TOTAL     := GREATEST(V_BASE_TOLL + V_SURCHARGE - NVL(V_DISCOUNT,0), 0);

    IF V_BALANCE < V_TOTAL THEN
        RAISE_APPLICATION_ERROR(-20002, 'Insufficient balance. Required: ' || V_TOTAL || ', Available: ' || V_BALANCE);
    END IF;

    -- Deduct balance
    UPDATE TAG SET BALANCE = BALANCE - V_TOTAL WHERE TAG_ID = P_TAG_ID;

    -- Record transaction
    SELECT TRANSACTION_SEQ.NEXTVAL INTO V_TXN_ID FROM DUAL;

    INSERT INTO TRANSACTION_HISTORY (
        TRANSACTION_ID, TAG_ID, SERVICE_ID, PLAZA_ID,
        ENTRY_TERMINAL_ID, EXIT_TERMINAL_ID,
        ENTRY_TIME, EXIT_TIME,
        DISTANCE_TRAVELLED, TOLL_RATE_PER_KM,
        BASE_TOLL, SURCHARGE, EXEMPTION_DISCOUNT,
        TOTAL_AMOUNT, DEDUCTED_BALANCE, STATUS, TRANSACTION_DATE
    ) VALUES (
        V_TXN_ID, P_TAG_ID, P_SERVICE_ID, P_PLAZA_ID,
        P_ENTRY_TERMINAL, P_EXIT_TERMINAL,
        P_ENTRY_TIME, P_EXIT_TIME,
        V_DISTANCE, V_RATE,
        V_BASE_TOLL, V_SURCHARGE, NVL(V_DISCOUNT,0),
        V_TOTAL, V_TOTAL, 'COMPLETED', SYSDATE
    );

    COMMIT;
    DBMS_OUTPUT.PUT_LINE('Transaction ' || V_TXN_ID || ' completed. Amount: ' || V_TOTAL);
END;
/

-- Procedure: Recharge tag balance
CREATE OR REPLACE PROCEDURE RECHARGE_TAG(
    P_TAG_ID        NUMBER,
    P_AMOUNT        NUMBER,
    P_METHOD        VARCHAR2 DEFAULT 'Cash',
    P_BANK_REF      VARCHAR2 DEFAULT NULL
)
IS
    V_RECHARGE_ID   NUMBER;
BEGIN
    UPDATE TAG SET BALANCE = BALANCE + P_AMOUNT WHERE TAG_ID = P_TAG_ID;

    SELECT RECHARGE_SEQ.NEXTVAL INTO V_RECHARGE_ID FROM DUAL;

    INSERT INTO ACCOUNT_RECHARGE (RECHARGE_ID, TAG_ID, RECHARGE_AMOUNT, RECHARGE_DATE, METHOD, BANK_REF, STATUS)
    VALUES (V_RECHARGE_ID, P_TAG_ID, P_AMOUNT, SYSDATE, P_METHOD, P_BANK_REF, 'SUCCESS');

    COMMIT;
    DBMS_OUTPUT.PUT_LINE('Recharge successful. New balance: ' || GET_BALANCE(P_TAG_ID));
END;
/



