INNER JOIN USERS U ON T.USER_ID = U.USER_ID;

-- Toll plazas on motorways
SELECT M.MOTORWAY_NAME, H.HIGHWAY_NAME, TP.PLAZA_NAME, TP.MILE_NO
FROM MOTORWAY M
INNER JOIN HIGHWAY H ON M.MOTORWAY_ID = H.MOTORWAY_ID
INNER JOIN TOLL_PLAZA TP ON H.HIGHWAY_ID = TP.HIGHWAY_ID;

-- ===================== LEFT JOIN =====================
-- All users and their feedback (including users with no feedback)
SELECT U.NAME, U.EMAIL, F.COMMENTS, F.RATING
FROM USERS U
LEFT JOIN FEEDBACK F ON U.USER_ID = F.USER_ID;

-- All plazas and employees
SELECT TP.PLAZA_NAME, E.EMPLOYEE_NAME, E.DESIGNATION
FROM TOLL_PLAZA TP
LEFT JOIN EMPLOYEE E ON TP.PLAZA_ID = E.PLAZA_ID;

-- ===================== RIGHT JOIN =====================
SELECT P.PLAZA_NAME, E.EMPLOYEE_NAME, E.DESIGNATION
FROM EMPLOYEE E
RIGHT JOIN TOLL_PLAZA P ON E.PLAZA_ID = P.PLAZA_ID;

-- ===================== GROUP BY =====================
-- Count tags by status
SELECT STATUS, COUNT(*) AS TAG_COUNT
FROM TAG
GROUP BY STATUS;

-- Total toll revenue per plaza
SELECT TP.PLAZA_NAME, SUM(TH.TOTAL_AMOUNT) AS TOTAL_REVENUE, COUNT(*) AS TXN_COUNT
FROM TRANSACTION_HISTORY TH
JOIN TOLL_PLAZA TP ON TH.PLAZA_ID = TP.PLAZA_ID
GROUP BY TP.PLAZA_NAME;

-- Average rating per user
SELECT U.NAME, AVG(F.RATING) AS AVG_RATING, COUNT(F.FEEDBACK_ID) AS FEEDBACK_COUNT
FROM USERS U
JOIN FEEDBACK F ON U.USER_ID = F.USER_ID
GROUP BY U.NAME;

-- ===================== HAVING =====================
SELECT STATUS, COUNT(*)
FROM TAG
GROUP BY STATUS
HAVING COUNT(*) >= 1;

-- Plazas with more than 1 transaction
SELECT TP.PLAZA_NAME, COUNT(*) AS TXN_COUNT
FROM TRANSACTION_HISTORY TH
JOIN TOLL_PLAZA TP ON TH.PLAZA_ID = TP.PLAZA_ID
GROUP BY TP.PLAZA_NAME
HAVING COUNT(*) > 0;

