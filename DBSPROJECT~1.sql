    CAMERA_LOCATION     VARCHAR2(100),
    STATUS              VARCHAR2(20) DEFAULT 'ACTIVE',
    CONSTRAINT FK_CAM_PLAZA FOREIGN KEY (PLAZA_ID) REFERENCES TOLL_PLAZA(PLAZA_ID)
);

-- =========================================================
-- 14. LANE
